function compareMatrix(m1, m2) {
  let total = 0;

  for (let i = 0; i < m1.length; i++) {
    for (let j = 0; j < m1[0].length; j++) {
      if (m1[i][j] != m2[i][j]) {
        total += 1;
      }
    }
  }

  return total;
}

